#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*ca;

	i = 0;
	ca = s;
	while (i < n)
	{
		ca[i] = '\0';
		i++;
	}
	return (s);
}
