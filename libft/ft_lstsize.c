#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*aux;
	int		i;

	i = 0;
	aux = lst;
	while (aux != NULL)
	{
		aux = aux->next;
		i++;
	}
	return (i);
}
