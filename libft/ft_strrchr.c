#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	l;
	int		i;

	l = c;
	i = ft_strlen(s);
	while (i >= 0)
	{
		if (s[i] == l)
			return ((char *)&s[i]);
		i--;
	}
	return (0);
}
